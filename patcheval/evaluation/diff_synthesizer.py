"""
Synthesize git-apply-compatible unified diffs from full-function patch strings.

Produces diffs equivalent to those generated by FuncReplacer + `git diff`, so
the output can be fed directly to `git apply` inside Docker evaluation containers.
"""
from __future__ import annotations

import difflib
import re
import textwrap


def _extract_base_indent(snippet: str) -> str:
    """Return the leading whitespace of the first non-empty line in snippet."""
    for line in snippet.splitlines():
        if line.strip():
            m = re.match(r'^(\s*)', line)
            return m.group(1) if m else ""
    return ""


def _normalize_indentation(new_content: str, target_indent: str) -> str:
    """
    Mirror FuncReplacer._replace_code_generic's indentation logic:
    dedent the LLM output then re-apply target_indent to every line.
    """
    try:
        dedented = textwrap.dedent(new_content)
    except Exception:
        dedented = new_content

    result_lines = [target_indent + line + "\n" for line in dedented.splitlines()]
    return "".join(result_lines)


def synthesize_diff(
    original_snippet: str,
    patched_function: str,
    file_path: str,
    start_line: int,
) -> str:
    """
    Build a git-apply-compatible unified diff from two function strings.

    Args:
        original_snippet:  Exact file content of the vulnerable function (from input.json).
        patched_function:  LLM-produced replacement (raw, may carry different indentation).
        file_path:         Repo-relative path, e.g. "aiohttp/web_middlewares.py".
        start_line:        1-based line in the file where the function starts.

    Returns:
        A unified diff string with a ``diff --git`` header and file-relative line
        numbers, ready for ``git apply``.  Returns an empty string when original
        and patched are identical after normalisation.
    """
    base_indent = _extract_base_indent(original_snippet)
    normalized = _normalize_indentation(patched_function, base_indent)

    orig_lines = original_snippet.splitlines(keepends=True)
    new_lines = normalized.splitlines(keepends=True)

    # Guarantee a trailing newline on the last line of each side so the hunk
    # counts are correct and `git apply` does not complain about missing newlines.
    if orig_lines and not orig_lines[-1].endswith('\n'):
        orig_lines[-1] += '\n'
    if new_lines and not new_lines[-1].endswith('\n'):
        new_lines[-1] += '\n'

    diff_lines = list(difflib.unified_diff(
        orig_lines,
        new_lines,
        fromfile=f"a/{file_path}",
        tofile=f"b/{file_path}",
        n=3,
    ))

    if not diff_lines:
        return ""

    # difflib numbers lines relative to the snippet (starting at 1).
    # Shift every hunk header by (start_line - 1) to make numbers file-relative.
    # This is correct even for multi-hunk diffs: each hunk's snippet-relative
    # positions already account for earlier insertions/deletions, so a constant
    # offset produces the correct file-relative positions.
    offset = start_line - 1
    fixed: list[str] = []
    for line in diff_lines:
        if line.startswith('@@'):
            m = re.match(r'@@ -(\d+)(?:,(\d+))? \+(\d+)(?:,(\d+))? @@(.*)', line)
            if m:
                old_start = int(m.group(1)) + offset
                old_count = m.group(2) if m.group(2) is not None else "1"
                new_start = int(m.group(3)) + offset
                new_count = m.group(4) if m.group(4) is not None else "1"
                rest = m.group(5)
                line = f"@@ -{old_start},{old_count} +{new_start},{new_count} @@{rest}\n"
        fixed.append(line)

    git_header = f"diff --git a/{file_path} b/{file_path}\n"
    return git_header + "".join(fixed)
